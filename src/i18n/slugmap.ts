/**
 * Get alternate URLs for a content page.
 */
export function getContentAlternates(
  type: 'guide' | 'review' | 'compare',
  currentSlug: string,
  currentLang: string,
): { lang: string; url: string }[] {
  const maps = { guide: guideSlugMap, review: reviewSlugMap, compare: compareSlugMap };
  const pathPrefixes = { guide: 'guide', review: 'review', compare: 'compare' };
  const map = maps[type];
  const prefix = pathPrefixes[type];

  let translationKey: string | null = null;
  for (const [key, slugs] of Object.entries(map)) {
    if (Object.values(slugs).includes(currentSlug)) {
      translationKey = key;
      break;
    }
  }

  if (!translationKey || !map[translationKey]) {
    return [{ lang: currentLang, url: `/${currentLang}/${prefix}/${currentSlug}/` }];


  }

  const slugs = map[translationKey];
  return Object.entries(slugs).map(([lang, slug]) => ({
    lang,
    url: `/${lang}/${prefix}/${slug}/`,
  }));
}

/**
 * Get the base (EN) slug for any localized slug.
 * Used to share assets (hero images) across all languages.
 * Falls back to the slug itself if not found in the map.
 */
export function getBaseSlug(localizedSlug: string): string {
  for (const [baseKey, slugs] of Object.entries(guideSlugMap)) {
    if (Object.values(slugs).includes(localizedSlug)) {
      return baseKey; // baseKey === EN slug
    }
  }
  return localizedSlug; // fallback: assume it's already EN
}
